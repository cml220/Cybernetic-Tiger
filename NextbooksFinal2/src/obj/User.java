package obj;

public class User {
	private String username;
	private String password;

	public User(String name, String password) throws Exception{
		username=name;
		this.password=password;
	}
	
	public String getUserName(){
		return username;
	}
	
	public String getPassword(){
		return password;		
	}
	
	
	
	
	
	
	
	
	
	
	
	

}
